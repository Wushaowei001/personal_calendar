$(document).ready(function(){
	$("li").children("div:first").hide();
	$("li").mouseover(function(){
		$(this).children("span").css("background-color","black");
		$(this).children(".menu").css("color","white");
		$(this).children("div:first").fadeIn();
	});
	$("li").mouseleave(function(){
		$(this).children(".menu").css("color","gray");
		$(this).children("div:first").fadeOut();
	});
});